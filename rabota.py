#На языке Питон написать программу, которая из файла читает объекты, разделенные  пробелами, выводит их на экран, производит
#заданную обработку и  результат выводит на экран. Обработка: через регулярные выражения найти произведение и количество
#натуральных нечетных трехразрядных десятичных чисел, в которых первые 2 цифры равны 12

import re

# Открываем файл для чтения
with open('test.txt', 'r') as file:
    data = file.read()

# Находим все числа, которые удовлетворяют условию
numbers = re.findall(r'\b12\d{1}\b', data)

# Создаём переменные для произведения и количества
product = 1
count = 0

# Проходим по найденным числам
for num in numbers:
    num_int = int(num)
    if num_int % 2 != 0:
        product *= num_int
        count += 1

# Выводим результат на экран
print("Найдено чисел:", count)
print("Произведение чисел:", product)